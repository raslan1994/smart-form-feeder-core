package com.raslan.sff.core.nn;

public class RecognitionResult {
	private Character bestFitCharacter;
	private double weight;
	
	public Character getBestFitCharacter() {
		return bestFitCharacter;
	}
	public void setBestFitCharacter(Character bestFitCharacter) {
		this.bestFitCharacter = bestFitCharacter;
	}
	public double getWeight() {
		return weight;
	}
	public void setWeight(double weight) {
		this.weight = weight;
	}
	
	
}
